package calendar;

public class User {
	private String username;
	private String name;
	
	public User(){
		
	}

	public User(String name, String username) {
		this.name = name;
		this.username = username;
	}

	public String getUsername() {
		return name;
	}
	
	public String getName() {
		return name;
	}

}
